$(document).ready(function() {
    // Using the jQuery load method to load the 'footer.html' file into the element with ID 'footer'.
    // This will replace the content of the 'footer' element with the contents of 'footer.html'.
    $('#footer').load('footer.html');

    // Using the jQuery load method to load the 'head.html' file into the element with ID 'head'.
    // This will replace the content of the 'head' element with the contents of 'head.html'.
    $('#head').load('head.html');
});
